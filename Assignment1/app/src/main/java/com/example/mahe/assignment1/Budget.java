public class Budget {
    private String inc,date,budg;




    public Budget(String inc, String date, String budg)
    {
        super();
        this.inc = inc;
        this.date = date;
        this.budg = budg;



    }

    public String getInc()
    {
        return inc;
    }
    public String getDate1()
    {
        return date;
    }
    public String getBudg()
    {
        return budg;
    }











}
